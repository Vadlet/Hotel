import UIKit

extension UIFont {
    
    static func avenir14() -> UIFont? {
        return UIFont(name: "avenir", size: 14)
    }
    
    static func avenirMedium16() -> UIFont? {
        return UIFont(name: "avenir-medium", size: 16)
    }
}
